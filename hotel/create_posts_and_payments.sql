-- ====================
-- 1. 결제 내역 테이블 생성
-- ====================
CREATE TABLE payments (
    payment_id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    reservation_id NUMBER REFERENCES reservations(reservation_id) ON DELETE CASCADE,
    member_num NUMBER REFERENCES users(member_num) ON DELETE CASCADE,
    amount NUMBER(12,2) NOT NULL,
    payment_method VARCHAR2(50) NOT NULL CHECK (payment_method IN ('CREDIT_CARD', 'DEBIT_CARD', 'BANK_TRANSFER', 'CASH')),
    payment_status VARCHAR2(20) DEFAULT 'COMPLETED' CHECK (payment_status IN ('PENDING', 'COMPLETED', 'FAILED', 'REFUNDED')),
    transaction_id VARCHAR2(100) UNIQUE,
    payment_date TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- 결제 내역 인덱스 생성
CREATE INDEX idx_payments_reservation ON payments(reservation_id);
CREATE INDEX idx_payments_member ON payments(member_num);
CREATE INDEX idx_payments_date ON payments(payment_date);
CREATE INDEX idx_payments_status ON payments(payment_status);

