-- 5. 확인 쿼리
-- ====================

-- 생성된 호텔 게시글 확인
SELECT post_id, title, city, address, view_count, file_name FROM posts WHERE post_type = 'HOTEL' ORDER BY post_id DESC;

-- 생성된 객실 타입 확인
SELECT p1.title as hotel_name, p2.title as room_type, p2.price, p2.room_count, p2.file_name 
FROM posts p1 
JOIN posts p2 ON p1.post_id = p2.parent_id 
WHERE p1.post_type = 'HOTEL' AND p2.post_type = 'ROOM_TYPE' 
ORDER BY p1.post_id, p2.price;

-- 결제 내역 확인
SELECT p.payment_id, p.amount, p.payment_method, p.payment_status, p.transaction_id, p.payment_date,
       u.name as customer_name, h.title as hotel_name
FROM payments p
JOIN reservations r ON p.reservation_id = r.reservation_id
JOIN users u ON p.member_num = u.member_num
JOIN posts h ON r.hotel_post_id = h.post_id
ORDER BY p.payment_date DESC;

-- 월별 결제 통계
SELECT TO_CHAR(payment_date, 'YYYY-MM') as month,
       COUNT(*) as total_payments,
       SUM(amount) as total_amount,
       AVG(amount) as avg_amount
FROM payments 
WHERE payment_status = 'COMPLETED'
GROUP BY TO_CHAR(payment_date, 'YYYY-MM')
ORDER BY month DESC;

