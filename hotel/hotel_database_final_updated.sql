-- ====================
-- 5. 예약 상태 자동 업데이트 시스템
-- ====================

-- 예약 상태 자동 업데이트 프로시저
CREATE OR REPLACE PROCEDURE update_reservation_status AS
BEGIN
    -- 체크인 날짜가 되면 CONFIRMED → CHECKED_IN
    UPDATE reservations 
    SET status = 'CHECKED_IN', 
        updated_at = CURRENT_TIMESTAMP
    WHERE status = 'CONFIRMED' 
    AND TRUNC(check_in_date) <= TRUNC(SYSDATE);
    
    -- 체크아웃 날짜가 되면 CHECKED_IN → CHECKED_OUT
    UPDATE reservations 
    SET status = 'CHECKED_OUT', 
        updated_at = CURRENT_TIMESTAMP
    WHERE status = 'CHECKED_IN' 
    AND TRUNC(check_out_date) <= TRUNC(SYSDATE);
    
    COMMIT;
    
    DBMS_OUTPUT.PUT_LINE('예약 상태 업데이트 완료: ' || TO_CHAR(SYSDATE, 'YYYY-MM-DD HH24:MI:SS'));
END;
/

-- 매일 자동 실행 스케줄러 생성
BEGIN
    DBMS_SCHEDULER.CREATE_JOB (
        job_name        => 'AUTO_UPDATE_RESERVATION_STATUS',
        job_type        => 'PLSQL_BLOCK',
        job_action      => 'BEGIN update_reservation_status; END;',
        start_date      => SYSTIMESTAMP,
        repeat_interval => 'FREQ=DAILY; BYHOUR=1; BYMINUTE=0; BYSECOND=0',
        enabled         => TRUE,
        comments        => '예약 상태 자동 업데이트 (매일 오전 1시)'
    );
END;
/

