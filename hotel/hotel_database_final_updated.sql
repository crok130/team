-- ==================== 개발용 테스트 쿼리 ====================
-- 매니저별 매출 요약 확인
SELECT '=== 호텔 매니저별 매출 현황 ===' as status FROM dual;

SELECT 
    u.name as manager_name,
    h.title as hotel_name,
    COUNT(r.reservation_id) as total_reservations,
    SUM(r.total_amount) as total_revenue,
    ROUND(AVG(r.total_amount), 0) as avg_revenue
FROM users u
JOIN posts h ON u.member_num = h.member_num
LEFT JOIN reservations r ON h.post_id = r.hotel_post_id
WHERE u.user_type = 'HOTEL_MANAGER' AND h.post_type = 'HOTEL'
GROUP BY u.member_num, u.name, h.post_id, h.title
ORDER BY total_revenue DESC;

-- 월별 매출 현황 확인 (실시간 계산)
SELECT '=== 월별 매출 현황 (실시간) ===' as status FROM dual;

SELECT 
    TO_CHAR(r.booking_date, 'YYYY-MM') as month,
    h.title as hotel_name,
    COUNT(r.reservation_id) as reservations_count,
    SUM(CASE WHEN r.status IN ('CONFIRMED', 'CHECKED_IN', 'CHECKED_OUT') 
        THEN r.total_amount ELSE 0 END) as monthly_revenue
FROM reservations r
JOIN posts h ON r.hotel_post_id = h.post_id
WHERE h.post_type = 'HOTEL'
GROUP BY TO_CHAR(r.booking_date, 'YYYY-MM'), h.title
ORDER BY month, monthly_revenue DESC;

