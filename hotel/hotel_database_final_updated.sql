-- 3. 예약 테이블 (IDENTITY 방식) - 상태 간소화
-- ====================
CREATE TABLE reservations (
    reservation_id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY, -- 자동 증가 기본키
    member_num NUMBER REFERENCES users(member_num) ON DELETE CASCADE, -- 예약자 회원번호
    hotel_post_id NUMBER REFERENCES posts(post_id), -- 호텔 게시글 ID
    room_post_id NUMBER REFERENCES posts(post_id), -- 객실타입 게시글 ID
    room_number VARCHAR2(10), -- 실제 배정된 객실 번호
    check_in_date DATE NOT NULL, -- 체크인 날짜
    check_out_date DATE NOT NULL, -- 체크아웃 날짜
    adults NUMBER(2) DEFAULT 1, -- 성인 인원수
    children NUMBER(2) DEFAULT 0, -- 아동 인원수
    total_nights NUMBER(5), -- 총 숙박 일수 (자동 계산)
    total_amount NUMBER(12,2) NOT NULL, -- 총 결제 금액
    booking_date TIMESTAMP DEFAULT CURRENT_TIMESTAMP, -- 예약 일시 (= 결제 일시)
    status VARCHAR2(20) DEFAULT 'CONFIRMED' CHECK (status IN ('CONFIRMED', 'CHECKED_IN', 'CHECKED_OUT')), -- 예약 상태 (간소화)
    special_requests CLOB, -- 특별 요청사항
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP, -- 생성 일시
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP, -- 수정 일시
    CONSTRAINT chk_dates CHECK (check_out_date > check_in_date) -- 체크아웃이 체크인보다 나중이어야 함
);

-- 숙박 일수 자동 계산 트리거
CREATE OR REPLACE TRIGGER trg_reservations_nights
    BEFORE INSERT OR UPDATE ON reservations
    FOR EACH ROW
BEGIN
    :NEW.total_nights := :NEW.check_out_date - :NEW.check_in_date; -- 숙박 일수 자동 계산
END;
